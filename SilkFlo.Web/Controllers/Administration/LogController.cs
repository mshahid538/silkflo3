        public async Task<IActionResult> Delete (string id)
        {
            if (id == null)
                return Redirect("/Administration/log");

            var log = await _unitOfWork.Logs.GetAsync(id);

            if (log == null)
                return Redirect("/Administration/Log");
            else
            {
                if (log.CreatedById != null)
                    log.CreatedBy = await _unitOfWork.Users.GetAsync(log.CreatedById);

                if (log.UpdatedById != null)
                    log.UpdatedBy = await _unitOfWork.Users.GetAsync(log.UpdatedById);

                var model = new Models.Log(log);

                return View("/Views/Administration/Log/delete.cshtml", model);
            }
        }
