        [HttpGet("/Administration/Log/Edit/{id}")]
        [HttpGet("/Administration/Log/Edit")]
        [Authorize(Policy = Policy.Administrator)]
        public async Task<IActionResult> Edit(string id)
        {
            Data.Core.Domain.Log log;

            if (string.IsNullOrWhiteSpace(id))
            {
                log = new Data.Core.Domain.Log
                {
                    Id = Guid.NewGuid().ToString(),
                };
            }
            else
            {
                log = await _unitOfWork.Logs.GetAsync(id);

                if (log == null)
                    return Redirect(
                        "/Administration/Log");
            }

            var model = new Models.Log(log);
            await model.GetCreatedAndUpdated(_unitOfWork);

            return View("/Views/Administration/Log/Edit.cshtml", model);
        }


