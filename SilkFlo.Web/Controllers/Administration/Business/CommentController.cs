            return View("/Views/Administration/Business/Comment/Edit.cshtml", model);
        }

        // Core parents and children for selector lists
        private async Task GetUsers_For_RecipientsAsync(Models.Business.Comment model)
        {
            var core = model.GetCore();

            await _unitOfWork.BusinessRecipients.GetForCommentAsync(core);
            await _unitOfWork.Users.GetUserForAsync(core.Recipients);

            var users = await _unitOfWork.Users.GetAllAsync();

            foreach (var user in users)
            {
                var selector = new Models.Selector
                {
                    Id = user.Id,
                    Name = user.ToString()
                };
                
                model.AllUsers_For_Recipients.Add(selector);

                foreach (var recipient in model.Recipients)
                    if (selector.Id == recipient.UserId)
                    {
                        selector.IsSelected = true;
                        break;
                    }
            }
        }




