        private async Task ProcessManyToMany_Users_For_Recipients(Models.Business.Comment model)
        {
            try
            {
                // Get the selected users for Recipients
                var selectedUsers_For_Recipients = model.AllUsers_For_Recipients.Where(x => x.IsSelected);

                // Un-assign users_For_Recipients from comment
                await _unitOfWork.BusinessRecipients.GetForCommentAsync(model.GetCore());


                var recipientsLength = model.GetCore().Recipients.Count();
                for (var i = recipientsLength - 1; i >= 0; i--)
                {
                    var recipient = model.GetCore().Recipients[i];

                    await _unitOfWork.BusinessRecipients
                                     .RemoveAsync(recipient);

                    model.GetCore()
                         .Recipients
                         .Remove(recipient);
                }

                // Assign users_For_Recipients to comment
                var users = await _unitOfWork.Users.GetAllAsync();
                int index = 0;
                foreach (var user in users)
                {
                    var recipient = model.AllUsers_For_Recipients[index];
                    if(recipient.IsSelected)
                    {
                        await _unitOfWork.BusinessRecipients
                                         .AddAsync(new Data.Core.Domain.Business.Recipient
                                            {
                                                Comment = model.GetCore(),
                                                UserId = user.Id
                                            });
                    }

                    index++;
                }
            }
            catch
            {
                throw;
            }
        }


