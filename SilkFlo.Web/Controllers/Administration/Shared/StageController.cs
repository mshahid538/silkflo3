        [HttpPost("/Administration/Shared/Stage/Edit")]
        [Authorize(Policy = Policy.Administrator)]
        public async Task<IActionResult> Edit(Models.Shared.Stage model)
        {
            try
            {
                // Get the userId
                var userId = GetUserId();


                // Is the userId present.
                // If not return to view.                
                if (userId == null)
                {
                    ModelState.AddModelError(
                            "Error",
                            "userId is missing.");

                    return View("/Views/Administration/Shared/Stage/Edit.cshtml", model);
                }


                // Is the model valid?
                // If not return to view.
                bool isValid = Validate(_unitOfWork, model);

                var feedback = new ViewModels.Feedback();
                feedback = await model.CheckUniqueAsync(_unitOfWork, feedback);
                if (!feedback.IsValid)
                {
                    isValid = false;
                    foreach (var feedbackElement in feedback.Elements)
                        model.Errors.Add(feedbackElement.Value);
                }

                if (!isValid)
                {
                    // Get parent: Shared.StageGroup -> StageGroup
                    await _unitOfWork.SharedStageGroups.GetStageGroupForAsync(model.GetCore());
                    model.StageGroup =
                        model.GetCore().StageGroup == null ?
                            null :
                            new Models.Shared.StageGroup(model.GetCore().StageGroup);

                    // Get list of potential parents: Shared.StageGroup -> StageGroup
                    var coreStageGroups = await _unitOfWork.SharedStageGroups.GetAllAsync();
                    foreach (var core in coreStageGroups)
                        model.StageGroups.Add(new Models.Shared.StageGroup(core));


                    return View("/Views/Administration/Shared/Stage/Edit.cshtml", model);
                }


                // Process the forms content.
                await _unitOfWork.SharedStages.AddAsync(model.GetCore());


                if(await _unitOfWork.CompleteAsync() == DataStoreResult.Success)
                    return Redirect(
                        "/Administration/Shared/Stage");

                return View("/Views/Administration/Shared/Stage/Edit.cshtml", model);
            }
            catch (Exception ex)
            {
                _unitOfWork.Log(ex);

                ModelState.AddModelError(
                    "Error",
                    Models.Log.Message_CouldNotSave);

                return View("/Views/Administration/Shared/Stage/Edit.cshtml", model);
            }
        }

